package cricketer.info.nonsensitive;

public class NonSensitiveInfo {
	//cricketer's non sensitive info which can be accessed by fans
	private int age = 20;
	private int runs = 22000;
	private int centuries = 120;
	private int boundaries = 4500;
	private int sixers = 230;
	private int wickets = 45;
	
	//to get age of the cricketer
	public int getAge() {
		return age;
	}
	
	//to get number of runs
	public int getRuns() {
		return runs;
	}
	
	//to get number of centuries
	public int getCenturies() {
		return centuries;
	}
	
	//to get number of boundaries
	public int getBoundaries() {
		return boundaries;
	}
	
	//to get number of sixers
	public int getSixers() {
		return sixers;
	}
	
	//to get the number of wickets
	public int getWickets() {
		return wickets;
	}
	

}
