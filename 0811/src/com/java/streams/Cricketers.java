package com.java.streams;

public class Cricketers {
	//instance variables of cricketer class
	String name;
	int age;
	int runs;
	int wickets;
	
	//constructor to initialize instance variables
	public Cricketers(String name, int age, int runs, int wickets) {
		this.name = name;
		this.age = age;
		this.runs = runs;
		this.wickets = wickets;
	}
}
